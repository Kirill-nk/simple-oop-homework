public class CharChar {
    public static void main(String[] args) {
        CharChar("fsfsdfdsfsdfewthrfjfh");
        CharChar("Опытный копирайтер знает, сколько времени у него уходит на написание килознака, поэтому может легко рассчитать, целесообразно ли ему браться за конкретный заказ. Новичку сложнее: он-то наверняка привык мерить текст страницами, как учат в плохих университетах. Эта статья поможет начинающему копирайтеру сделать вывод: много это или мало – 1000 знаков?");
    }

    public static String CharChar(String string) {
        CharChar[] charChar = new CharChar[string.length()];
        return charChar.toString();
    }
}
